def paginate(items_per_page, what):
    pages = []

    cur_page = []

    what.sort()
    for file in enumerate(what):
        if file[0] % items_per_page == 0:
            if cur_page:
                pages.append(cur_page)
                cur_page = []
        cur_page.append(file[1])
    pages.append(cur_page)

    return pages